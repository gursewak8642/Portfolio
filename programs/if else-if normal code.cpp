// WAP to to demonstrate the use of nested if structure
#include<stdio.h>
int main(){
	
	int x,y;
	  printf("\n Enter two numbers ");
	  scanf("%d %d", &x, &y);
	if(x==y)
	  printf("\n The two numbers are equal");
	else if(x>y)
	  printf("\n %d is greater than %d ", x,y);
	else
	  printf("\n %d is smaller than %d", x,y);
	 return 0;
	      
	
	
	
	
	
}
